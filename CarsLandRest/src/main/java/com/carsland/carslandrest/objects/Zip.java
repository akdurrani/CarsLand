package com.carsland.carslandrest.objects;

public class Zip {
	private int zip;
	private String city;
	private String state;
	
	public int getZip() {
		return zip;
	}
	
	public String getCity() {
		return city;
	}
	
	public String getState() {
		return state;
	}
	
	public void setZip(int zip) {
		this.zip = zip;
	}
	
	public void setCity(String city) {
		this.city = city;
	}
	
	public void setState(String state) {
		this.state = state;
	}


}
